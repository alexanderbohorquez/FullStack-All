//*Calculadora de Valor Medio
// En este ejercicio vas a pedir al usuario que teclee tres números enteros y el script
// mostrará como resultado el valor medio de los tres.
// Recuerda que la media de tres números se calcula sumando los tres y dividiendo entre 3.

//* Asignación de valores a las variables
let num1 = 3;   // El primer número es 3
let num2 = 2;   // El segundo número es 2
let num3 = 4;   // El tercer número es 4

//* Cálculo del promedio
// Se suman los valores de los tres números y se dividen por la cantidad de números para obtener el promedio
let resultado = (num1 + num2 + num3) / 3;

//* Impresión del resultado
console.log(resultado);  // Se imprime el resultado (el promedio) en la consola = 3 !!!!


