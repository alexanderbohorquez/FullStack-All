//*Calculadora de Consumo de Combustible  
// Escribe un programa que solicite al usuario ingresar el número de kilómetros 
// recorridos por su coche y el número de litros consumidos . 
// El script debe mostrar el consumo de combustible por kilómetro. 
// Un problema matemático muy simple numero de litros dividido por número de kilómetros.

//* Asignación de valores a las variables
let numLitros = 20;   // Número de litros de combustible
let numKm = 80;       // Número de kilómetros recorridos

//* Cálculo del consumo de combustible por kilómetro
// Se divide el número de litros por el número de kilómetros para obtener el consumo por kilómetro
let consumo = numLitros / numKm;

//* Impresión del resultado
console.log(consumo + "l/km");  // Se imprime el consumo de combustible por kilómetro en la consola
