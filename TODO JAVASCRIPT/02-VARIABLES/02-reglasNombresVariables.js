//* REGLAS DE NOMBRES DE VARIABLES 

//? En JavaScript, los nombres de variables pueden incluir letras, $ y _ como primeros caracteres, seguidos opcionalmente por números.

// Definición de una variable '$miNumero1' y asignación del valor 10.
let $miNumero1 = 10;
// Imprime en la consola el valor de '$miNumero1'.
console.log($miNumero1);

// Definición de una variable '_miNumero2' y asignación del valor 20.
let _miNumero2 = 20;
// Imprime en la consola el valor de '_miNumero2'.
console.log(_miNumero2);

// Definición de una variable 'miNumero3' y asignación del valor 30.
let miNumero3 = 30;
// Imprime en la consola el valor de 'miNumero3'.
console.log(miNumero3);

//* JavaScript es sensible a mayúsculas y minúsculas.
