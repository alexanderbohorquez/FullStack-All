//* TIPOS DE DATOS EN JAVASCRIPT

//? Entero: Representa números enteros sin decimales.
let miEntero = 10;
console.log(miEntero); 
console.log(typeof miEntero); 

//? Flotante: Representa números con decimales.
let miFlotante = 7.9;
console.log(miFlotante); 
console.log(typeof miFlotante); 

//? Cadena de caracteres: Representa texto.
let miCadena = "Hola";
console.log(miCadena);
console.log(typeof miCadena); 

//? Booleano: Representa un valor verdadero o falso.
let miBoolean = true;
console.log(miBoolean); 
console.log(typeof miBoolean); 

//? Valor nulo: Representa la ausencia intencional de valor.
let miNull = null;
console.log(miNull); 
console.log(typeof miNull); 

//? Valor indefinido: Representa una variable que no ha sido asignada o inicializada.
let miUndefined = undefined;
console.log(miUndefined); 
console.log(typeof miUndefined); 
