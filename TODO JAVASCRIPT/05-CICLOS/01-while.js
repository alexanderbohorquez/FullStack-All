//* Ciclo while
// Imprimir los valores del 0 al 5
let contador = 0;

while (contador < 10) {
    console.log(contador++); // Imprime el valor actual del contador en cada iteración
    contador ++; // Incrementa el contador en 1 en cada iteración
}

let i = 0;
let x = 0;

while (i < 10) {
    i ++; // Incrementa i en 1 en cada iteración
    x += i; // Agrega el valor de i a x en cada iteración
    console.log(i); // Imprime el valor actual de i en cada iteración
    console.log(x); // Imprime el valor actual de x en cada iteración
}
