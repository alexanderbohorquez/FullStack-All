//* OPERADORES DE ASIGNACION    

let miNumero = 10; 
console.log(miNumero);

miNumero = 20;
console.log(miNumero);

//* operado +=
miNumero += 5;
console.log(miNumero);

//* operador -=
miNumero -= 3;
console.log(miNumero);

//* operador *=
miNumero *= 2;
console.log(miNumero);

//* operador /=
miNumero /= 4;
console.log(miNumero);

//* operador %= 
miNumero %= 2;
console.log(miNumero);

//* operador **=
miNumero = 8;
miNumero **= 2; //* 8 * 8 = 64
console.log(miNumero);