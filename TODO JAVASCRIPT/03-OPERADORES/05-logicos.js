//* Operadores logicos 
let a = true;
let b = false;
console.log(a);
console.log(b);

//* Operadores logicos && (and o Y)
// regresan verdadero si ambos operandos son verdaderos
console.log(`${a} && ${b} -> ${a && b}`);

//* operador logico || (or o O)
// Regresa verdadero si cualquiera de loa valores es verdadero
console.log(`${a} || ${b} -> ${a || b }`);

//* Operador ! (not o NO)
//  Invierte el valor original, true -> false, false -> true 
console.log(`${a} -> ${!a}`);