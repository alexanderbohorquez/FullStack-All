// Definimos una variable
let miNumero = 10;

// Operador ternario:
// Si miNumero es mayor que 0,
// se ejecuta el código después del "?" (console.log(`Positivo ${miNumero}`));
// de lo contrario, se ejecuta el código después de ":" (console.log(`negativo ${miNumero}`));
(miNumero > 0) ? console.log(`Positivo ${miNumero}`) : console.log(`negativo ${miNumero}`);
